package Entity;

import java.sql.Time;

public class Movie {
    int id;
    String name;
    String description;
    String image;
    String videoTrailer;
    int hour;
    int minute;
    String duration;

    public Movie() {
    }

    public Movie(String name, String description, String image, int hour, int minute, String videoTrailer) {
        this.name = name;
        this.description = description;
        this.image = image;
        this.hour = hour;
        this.minute = minute;
        this.duration = new Time(hour, minute, 0).toString();
        this.videoTrailer = videoTrailer;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public int getHour() {
        return hour;
    }

    public void setHour(int hour) {
        this.hour = hour;
    }

    public int getMinute() {
        return minute;
    }

    public void setMinute(int minute) {
        this.minute = minute;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDuration() {
        return duration;
    }

    public void setDuration(String duration) {
        this.duration = duration;
    }

    public String getVideoTrailer() {
        return videoTrailer;
    }

    public void setVideoTrailer(String videoTrailer) {
        this.videoTrailer = videoTrailer;
    }
    
}
